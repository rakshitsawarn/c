#include<stdio.h>
int main(){
    int a = 4;
    int b = 3;
    // Addition
    int sum = a+b;
    printf("The sum of a and b is: %d\n",sum);
    // substraction
    int substraction = a-b;
    printf("The difference of a and b is: %d\n",substraction);
    // multiply
    int multiply = a * b;
    printf("The product of a and b is: %d\n",multiply);
    // divide
    int divide = a/b;
    printf("The division of a and b is: %d\n",divide);
    //remainder
    int remainder = a % b;
    printf("The Remainder of a and b is: %d\n",remainder);
    return 0;
}